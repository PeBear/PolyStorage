    private void selectImage() {
        JFileChooser jfc = new JFileChooser();
        String pathAva = ProcessImage.getPathAva();
        jfc.setCurrentDirectory(new File(pathAva));
        int ok = jfc.showOpenDialog(this);
        if (ok == JFileChooser.APPROVE_OPTION) {
            try {
                hinh = jfc.getSelectedFile().getName();
                Path source = Paths.get(jfc.getSelectedFile().getAbsolutePath());
                Path desti = Paths.get(pathAva + hinh);
                Files.copy(source, desti, StandardCopyOption.REPLACE_EXISTING);
                ProcessImage.setAvatar(hinh, lblHinh);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }

    private void showDetail() {
        txtMaNV.setText(nv.getMaNv());
        txtHoTen.setText(nv.getHoTen());
        txtEmail.setText(nv.getEmail());
        txtSDT.setText(nv.getSdt());
        txtMatKhau.setText(nv.getMatKhau());
        // True and index 0 is Male, False and index 1 is Female
        if (nv.isGioiTinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        if (nv.getVaiTro().equals("Nhân Viên")) {
            cboVaiTro.setSelectedIndex(0);
        } else {
            cboVaiTro.setSelectedIndex(1);
        }
        ProcessImage.setAvatar(nv.getHinh(), lblHinh);
    }

    private String checkInput() {
        if (txtMaNV.getText().isEmpty()) {
            txtMaNV.setBackground(Color.yellow);
            txtMaNV.requestFocus();
            return "Mã nhân viên trống.";
        } else if (!txtMaNV.getText().matches("[a-zA-Z0-9]+")) {
            txtMaNV.setBackground(Color.yellow);
            txtMaNV.requestFocus();
            return "Sai định dạng mã nhân viên.";
        }

        if (txtMatKhau.getText().isEmpty()) {
            txtMatKhau.setBackground(Color.yellow);
            txtMatKhau.requestFocus();
            return "Mật khẩu trống.";
        } else if (!txtMatKhau.getText().matches("[a-zA-Z0-9]{6,}")) {
            txtMatKhau.setBackground(Color.yellow);
            txtMatKhau.requestFocus();
            return "Mật khẩu phải trên 6 kí tự.";
        }

        if (txtHoTen.getText().isEmpty()) {
            txtHoTen.setBackground(Color.yellow);
            txtHoTen.requestFocus();
            return "Họ tên trống.";
        } else if (!txtHoTen.getText().matches("[\\p{L} ]+")) {
            txtHoTen.setBackground(Color.yellow);
            txtHoTen.requestFocus();
            return "Sai định dạng họ tên.";
        }

        if (txtEmail.getText().isEmpty()) {
            txtEmail.setBackground(Color.yellow);
            txtEmail.requestFocus();
            return "Email trống.";
        } else if (!txtEmail.getText().matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
            txtEmail.setBackground(Color.yellow);
            txtEmail.requestFocus();
            return "Sai định dạng email.";
        }

        if (txtSDT.getText().isEmpty()) {
            txtSDT.setBackground(Color.yellow);
            txtSDT.requestFocus();
            return "Số điện thoại trống.";
        } else if (!txtSDT.getText().matches("[0-9]{10}")) {
            txtSDT.setBackground(Color.yellow);
            txtSDT.requestFocus();
            return "Sai định dạng số điện thoại.";
        }

        return "";
    }

    private void setWhite() {
        txtMaNV.setBackground(Color.white);
        txtHoTen.setBackground(Color.white);
        txtMatKhau.setBackground(Color.white);
        txtEmail.setBackground(Color.white);
        txtSDT.setBackground(Color.white);
    }

    private NhanVien getModel() {
        String maNv = txtMaNV.getText();
        String hoTen = txtHoTen.getText();
        String email = txtEmail.getText();
        String sdt = txtSDT.getText();
        String matKhau = txtMatKhau.getText();
        //true is Nam and false is Nu
        boolean gioiTinh = false;
        if (rdoNam.isSelected()) {
            gioiTinh = true;
        }
        String vaiTro = cboVaiTro.getSelectedItem().toString();
        String hinh = lblHinh.getToolTipText();

        NhanVien nv = new NhanVien(maNv, hoTen, email, gioiTinh, sdt, matKhau, hinh, vaiTro);
        return nv;
    }
