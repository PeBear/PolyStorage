    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        this.setWhite();
        String loi = this.checkInput();
        if (loi.equals("")) {
            NhanVien model = this.getModel();
            if (capNhat) {
                if (nhanVienDAOImpl.updateNhanVien(model)) {
                    DialogHelper.showMessageDialog(this, "Câp nhật nhân viên thành công.");
                    PanelDanhSachNhanVien.reload.fillToTable(null);
                } else {
                    DialogHelper.showMessageDialog(this, "Câp nhật nhân viên thất bại.");
                }
            } else {
                if (nhanVienDAOImpl.insertNhanVien(model)) {
                    DialogHelper.showMessageDialog(this, "Thêm nhân viên thành công.");
                    PanelDanhSachNhanVien.reload.fillToTable(null);
                } else {
                    DialogHelper.showMessageDialog(this, "Thêm nhân viên thất bại.");
                }
            }
        } else {
            DialogHelper.showMessageDialog(this, loi);
        }
    }//GEN-LAST:event_btnSaveMouseClicked

