    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        this.setWhite();
        String loi = this.checkInput();
        if (loi.equals("")) {
            NhaCungCap model = this.getModel();
            if (capNhat) {
                if (nhaCungCapDAOImpl.updateNhaCungCap(model)) {
                    DialogHelper.showMessageDialog(this, "Câp nhật nhà cung cấp thành công.");
                    PanelDanhSachNhaCungCap.reload.fillToTable(null);
                } else {
                    DialogHelper.showMessageDialog(this, "Câp nhật nhà cung cấp thất bại.");
                }
            } else {
                if (nhaCungCapDAOImpl.insertNhaCungCap(model)) {
                    DialogHelper.showMessageDialog(this, "Thêm nhà cung cấp thành công.");
                    PanelDanhSachNhaCungCap.reload.fillToTable(null);
                } else {
                    DialogHelper.showMessageDialog(this, "Thêm nhà cung cấp thất bại.");
                }
            }
        } else {
            DialogHelper.showMessageDialog(this, loi);
        }
    }//GEN-LAST:event_btnSaveMouseClicked

