    private String checkInput() {
        if (txtMaNCC.getText().isEmpty()) {
            txtMaNCC.setBackground(Color.yellow);
            txtMaNCC.requestFocus();
            return "Mã nhà cung cấp trống.";
        } else if (!txtMaNCC.getText().matches("[a-zA-Z0-9]+")) {
            txtMaNCC.setBackground(Color.yellow);
            txtMaNCC.requestFocus();
            return "Sai định dạng mã nhà cung cấp.";
        }

        if (txtTenNCC.getText().isEmpty()) {
            txtTenNCC.setBackground(Color.yellow);
            txtTenNCC.requestFocus();
            return "Tên nhà cung cấp trống.";
        } else if (!txtTenNCC.getText().matches("[\\p{L} ]+")) {
            txtTenNCC.setBackground(Color.yellow);
            txtTenNCC.requestFocus();
            return "Sai định dạng tên nhà cung cấp.";
        }

        if (txtEmail.getText().isEmpty()) {
            txtEmail.setBackground(Color.yellow);
            txtEmail.requestFocus();
            return "Email trống.";
        } else if (!txtEmail.getText().matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
            txtEmail.setBackground(Color.yellow);
            txtEmail.requestFocus();
            return "Sai định dạng email.";
        }

        if (txtSDT.getText().isEmpty()) {
            txtSDT.setBackground(Color.yellow);
            txtSDT.requestFocus();
            return "Số điện thoại trống.";
        } else if (!txtSDT.getText().matches("[0-9]{10}")) {
            txtSDT.setBackground(Color.yellow);
            txtSDT.requestFocus();
            return "Sai định dạng số điện thoại.";
        }
        
        if (txtDiaChi.getText().isEmpty()) {
            txtDiaChi.setBackground(Color.yellow);
            txtDiaChi.requestFocus();
            return "Địa chỉ trống.";
        } else if (!txtDiaChi.getText().matches("[\\p{L} ]+")) {
            txtDiaChi.setBackground(Color.yellow);
            txtDiaChi.requestFocus();
            return "Sai định dạng địa chỉ.";
        }

        return "";
    }

    private void setWhite() {
        txtMaNCC.setBackground(Color.white);
        txtTenNCC.setBackground(Color.white);
        txtDiaChi.setBackground(Color.white);
        txtEmail.setBackground(Color.white);
        txtSDT.setBackground(Color.white);
    }

    private NhaCungCap getModel() {
        String maNCC = txtMaNCC.getText();
        String tenNCC = txtTenNCC.getText();
        String email = txtEmail.getText();
        String sdt = txtSDT.getText();
        String diaChi = txtDiaChi.getText();

        NhaCungCap nhaCungCap = new NhaCungCap(maNCC, tenNCC, email, sdt, diaChi);
        return nhaCungCap;
    }
